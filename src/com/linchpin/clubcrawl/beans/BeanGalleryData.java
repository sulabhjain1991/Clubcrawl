package com.linchpin.clubcrawl.beans;

public class BeanGalleryData
{
	private String	id;
	private String	gallery_img;
	private String	club_url;
	private String	gallery_caption;
	private String	id_location;
	private String	status;
	private String	offer;
	private String	address;

	public String getId()
	{
		return id;
	}

	public void setId(String id)
	{
		this.id = id;
	}

	public String getGallery_img()
	{
		return gallery_img;
	}

	public void setGallery_img(String gallery_img)
	{
		this.gallery_img = gallery_img;
	}

	public String getClub_url()
	{
		return club_url;
	}

	public void setClub_url(String club_url)
	{
		this.club_url = club_url;
	}

	public String getGallery_caption()
	{
		return gallery_caption;
	}

	public void setGallery_caption(String gallery_caption)
	{
		this.gallery_caption = gallery_caption;
	}

	public String getId_location()
	{
		return id_location;
	}

	public void setId_location(String id_location)
	{
		this.id_location = id_location;
	}

	public String getStatus()
	{
		return status;
	}

	public void setStatus(String status)
	{
		this.status = status;
	}

	public String getOffer()
	{
		return offer;
	}

	public void setOffer(String offer)
	{
		this.offer = offer;
	}

	public String getAddress()
	{
		return address;
	}

	public void setAddress(String address)
	{
		this.address = address;
	}
}
