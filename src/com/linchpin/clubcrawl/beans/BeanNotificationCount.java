package com.linchpin.clubcrawl.beans;

public class BeanNotificationCount
{
	private String msg_count;
	private String request_count;
	private String notification_count;
	public String getMsg_count() {
		return msg_count;
	}
	public void setMsg_count(String msg_count) {
		this.msg_count = msg_count;
	}
	public String getRequest_count() {
		return request_count;
	}
	public void setRequest_count(String request_count) {
		this.request_count = request_count;
	}
	public String getNotification_count() {
		return notification_count;
	}
	public void setNotification_count(String notification_count) {
		this.notification_count = notification_count;
	}

}
