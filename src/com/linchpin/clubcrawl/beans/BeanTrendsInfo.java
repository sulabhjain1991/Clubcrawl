package com.linchpin.clubcrawl.beans;

public class BeanTrendsInfo {

	private String venue_name;
	private String address;
	private String venue_image;
	private String venue_num;
	public String getVenue_name() {
		return venue_name;
	}
	public void setVenue_name(String venue_name) {
		this.venue_name = venue_name;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getVenue_image() {
		return venue_image;
	}
	public void setVenue_image(String venue_image) {
		this.venue_image = venue_image;
	}
	public String getVenue_num() {
		return venue_num;
	}
	public void setVenue_num(String venue_num) {
		this.venue_num = venue_num;
	}
	
	
	
}
